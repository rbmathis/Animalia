namespace AnimalKingdom.root.Metazoa.Chordata.Actinopteri.Labriformes.Odacidae;

/// <summary>
/// Interface defining characteristics of Odacidae (family).
/// </summary>
public interface IOdacidae
{
    /// <inheritdoc />
    string FamilyName { get; }

    /// <inheritdoc />
    string[] FamilyCharacteristics { get; }

    /// <inheritdoc />
    bool HasNonRetractableClaws { get; }

    /// <inheritdoc />
    bool IsDigitigrade { get; }

    /// <inheritdoc />
    int FrontToeCount { get; }

    /// <inheritdoc />
    int HindToeCount { get; }

    /// <inheritdoc />
    string[] CommunicationMethods { get; }

}
