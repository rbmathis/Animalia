namespace AnimalKingdom.root.Metazoa.Chordata.Actinopteri.Characiformes.Alestidae;

/// <summary>
/// Interface defining characteristics of Alestidae (family).
/// </summary>
public interface IAlestidae
{
    /// <inheritdoc />
    string FamilyName { get; }

    /// <inheritdoc />
    string[] FamilyCharacteristics { get; }

    /// <inheritdoc />
    bool HasNonRetractableClaws { get; }

    /// <inheritdoc />
    bool IsDigitigrade { get; }

    /// <inheritdoc />
    int FrontToeCount { get; }

    /// <inheritdoc />
    int HindToeCount { get; }

    /// <inheritdoc />
    string[] CommunicationMethods { get; }

}
