namespace AnimalKingdom.root.Metazoa.Chordata.Actinopteri.Anabantiformes.Nandidae;

/// <summary>
/// Interface defining characteristics of Nandidae (family).
/// </summary>
public interface INandidae
{
    /// <inheritdoc />
    string FamilyName { get; }

    /// <inheritdoc />
    string[] FamilyCharacteristics { get; }

    /// <inheritdoc />
    bool HasNonRetractableClaws { get; }

    /// <inheritdoc />
    bool IsDigitigrade { get; }

    /// <inheritdoc />
    int FrontToeCount { get; }

    /// <inheritdoc />
    int HindToeCount { get; }

    /// <inheritdoc />
    string[] CommunicationMethods { get; }

}
