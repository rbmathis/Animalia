namespace AnimalKingdom.root.Metazoa.Chordata.Actinopteri.Perciformes.Pholidae;

/// <summary>
/// Interface defining characteristics of Pholidae (family).
/// </summary>
public interface IPholidae
{
    /// <inheritdoc />
    string FamilyName { get; }

    /// <inheritdoc />
    string[] FamilyCharacteristics { get; }

    /// <inheritdoc />
    bool HasNonRetractableClaws { get; }

    /// <inheritdoc />
    bool IsDigitigrade { get; }

    /// <inheritdoc />
    int FrontToeCount { get; }

    /// <inheritdoc />
    int HindToeCount { get; }

    /// <inheritdoc />
    string[] CommunicationMethods { get; }

}
