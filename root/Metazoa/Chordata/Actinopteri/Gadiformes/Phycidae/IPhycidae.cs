namespace AnimalKingdom.root.Metazoa.Chordata.Actinopteri.Gadiformes.Phycidae;

/// <summary>
/// Interface defining characteristics of Phycidae (family).
/// </summary>
public interface IPhycidae
{
    /// <inheritdoc />
    string FamilyName { get; }

    /// <inheritdoc />
    string[] FamilyCharacteristics { get; }

    /// <inheritdoc />
    bool HasNonRetractableClaws { get; }

    /// <inheritdoc />
    bool IsDigitigrade { get; }

    /// <inheritdoc />
    int FrontToeCount { get; }

    /// <inheritdoc />
    int HindToeCount { get; }

    /// <inheritdoc />
    string[] CommunicationMethods { get; }

}
