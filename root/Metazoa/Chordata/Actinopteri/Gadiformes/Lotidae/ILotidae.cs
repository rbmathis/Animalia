namespace AnimalKingdom.root.Metazoa.Chordata.Actinopteri.Gadiformes.Lotidae;

/// <summary>
/// Interface defining characteristics of Lotidae (family).
/// </summary>
public interface ILotidae
{
    /// <inheritdoc />
    string FamilyName { get; }

    /// <inheritdoc />
    string[] FamilyCharacteristics { get; }

    /// <inheritdoc />
    bool HasNonRetractableClaws { get; }

    /// <inheritdoc />
    bool IsDigitigrade { get; }

    /// <inheritdoc />
    int FrontToeCount { get; }

    /// <inheritdoc />
    int HindToeCount { get; }

    /// <inheritdoc />
    string[] CommunicationMethods { get; }

}
