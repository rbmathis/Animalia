namespace AnimalKingdom.root.Metazoa.Chordata.Actinopteri.Stomiiformes.Diplophidae;

/// <summary>
/// Interface defining characteristics of Diplophidae (family).
/// </summary>
public interface IDiplophidae
{
    /// <inheritdoc />
    string FamilyName { get; }

    /// <inheritdoc />
    string[] FamilyCharacteristics { get; }

    /// <inheritdoc />
    bool HasNonRetractableClaws { get; }

    /// <inheritdoc />
    bool IsDigitigrade { get; }

    /// <inheritdoc />
    int FrontToeCount { get; }

    /// <inheritdoc />
    int HindToeCount { get; }

    /// <inheritdoc />
    string[] CommunicationMethods { get; }

}
