namespace AnimalKingdom.root.Metazoa.Chordata.Actinopteri.Cypriniformes.Tincidae;

/// <summary>
/// Interface defining characteristics of Tincidae (family).
/// </summary>
public interface ITincidae
{
    /// <inheritdoc />
    string FamilyName { get; }

    /// <inheritdoc />
    string[] FamilyCharacteristics { get; }

    /// <inheritdoc />
    bool HasNonRetractableClaws { get; }

    /// <inheritdoc />
    bool IsDigitigrade { get; }

    /// <inheritdoc />
    int FrontToeCount { get; }

    /// <inheritdoc />
    int HindToeCount { get; }

    /// <inheritdoc />
    string[] CommunicationMethods { get; }

}
