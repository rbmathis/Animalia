namespace AnimalKingdom.root.Metazoa.Chordata.Actinopteri.Caproiformes.Caproidae;

/// <summary>
/// Interface defining characteristics of Caproidae (family).
/// </summary>
public interface ICaproidae
{
    /// <inheritdoc />
    string FamilyName { get; }

    /// <inheritdoc />
    string[] FamilyCharacteristics { get; }

    /// <inheritdoc />
    bool HasNonRetractableClaws { get; }

    /// <inheritdoc />
    bool IsDigitigrade { get; }

    /// <inheritdoc />
    int FrontToeCount { get; }

    /// <inheritdoc />
    int HindToeCount { get; }

    /// <inheritdoc />
    string[] CommunicationMethods { get; }

}
