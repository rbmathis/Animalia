namespace AnimalKingdom.root.Metazoa.Chordata.Actinopteri.Gonorynchiformes.Chanidae;

/// <summary>
/// Interface defining characteristics of Chanidae (family).
/// </summary>
public interface IChanidae
{
    /// <inheritdoc />
    string FamilyName { get; }

    /// <inheritdoc />
    string[] FamilyCharacteristics { get; }

    /// <inheritdoc />
    bool HasNonRetractableClaws { get; }

    /// <inheritdoc />
    bool IsDigitigrade { get; }

    /// <inheritdoc />
    int FrontToeCount { get; }

    /// <inheritdoc />
    int HindToeCount { get; }

    /// <inheritdoc />
    string[] CommunicationMethods { get; }

}
