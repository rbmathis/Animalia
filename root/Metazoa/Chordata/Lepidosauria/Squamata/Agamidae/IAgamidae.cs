namespace AnimalKingdom.root.Metazoa.Chordata.Lepidosauria.Squamata.Agamidae;

/// <summary>
/// Interface defining characteristics of Agamidae (family).
/// </summary>
public interface IAgamidae
{
    /// <inheritdoc />
    string FamilyName { get; }

    /// <inheritdoc />
    string[] FamilyCharacteristics { get; }

    /// <inheritdoc />
    bool HasNonRetractableClaws { get; }

    /// <inheritdoc />
    bool IsDigitigrade { get; }

    /// <inheritdoc />
    int FrontToeCount { get; }

    /// <inheritdoc />
    int HindToeCount { get; }

    /// <inheritdoc />
    string[] CommunicationMethods { get; }

}
