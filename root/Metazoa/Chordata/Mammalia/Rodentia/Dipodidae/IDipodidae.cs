namespace AnimalKingdom.root.Metazoa.Chordata.Mammalia.Rodentia.Dipodidae;

/// <summary>
/// Interface defining characteristics of Dipodidae (family).
/// </summary>
public interface IDipodidae
{
    /// <inheritdoc />
    string FamilyName { get; }

    /// <inheritdoc />
    string[] FamilyCharacteristics { get; }

    /// <inheritdoc />
    bool HasNonRetractableClaws { get; }

    /// <inheritdoc />
    bool IsDigitigrade { get; }

    /// <inheritdoc />
    int FrontToeCount { get; }

    /// <inheritdoc />
    int HindToeCount { get; }

    /// <inheritdoc />
    string[] CommunicationMethods { get; }

}
