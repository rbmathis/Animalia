namespace AnimalKingdom.root.Metazoa.Chordata.Mammalia.Carnivora.Mustelidae;

/// <summary>
/// Interface defining characteristics of Mustelidae (family).
/// </summary>
public interface IMustelidae
{
    /// <inheritdoc />
    string FamilyName { get; }

    /// <inheritdoc />
    string[] FamilyCharacteristics { get; }

    /// <inheritdoc />
    bool HasNonRetractableClaws { get; }

    /// <inheritdoc />
    bool IsDigitigrade { get; }

    /// <inheritdoc />
    int FrontToeCount { get; }

    /// <inheritdoc />
    int HindToeCount { get; }

    /// <inheritdoc />
    string[] CommunicationMethods { get; }

}
