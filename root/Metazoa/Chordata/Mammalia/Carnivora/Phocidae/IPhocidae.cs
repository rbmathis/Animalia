namespace AnimalKingdom.root.Metazoa.Chordata.Mammalia.Carnivora.Phocidae;

/// <summary>
/// Interface defining characteristics of Phocidae (family).
/// </summary>
public interface IPhocidae
{
    /// <inheritdoc />
    string FamilyName { get; }

    /// <inheritdoc />
    string[] FamilyCharacteristics { get; }

    /// <inheritdoc />
    bool HasNonRetractableClaws { get; }

    /// <inheritdoc />
    bool IsDigitigrade { get; }

    /// <inheritdoc />
    int FrontToeCount { get; }

    /// <inheritdoc />
    int HindToeCount { get; }

    /// <inheritdoc />
    string[] CommunicationMethods { get; }

}
