namespace AnimalKingdom.root.Metazoa.Chordata.Crocodylia.Crocodylidae;

/// <summary>
/// Interface defining characteristics of Crocodylidae (family).
/// </summary>
public interface ICrocodylidae
{
    /// <inheritdoc />
    string FamilyName { get; }

    /// <inheritdoc />
    string[] FamilyCharacteristics { get; }

    /// <inheritdoc />
    bool HasNonRetractableClaws { get; }

    /// <inheritdoc />
    bool IsDigitigrade { get; }

    /// <inheritdoc />
    int FrontToeCount { get; }

    /// <inheritdoc />
    int HindToeCount { get; }

    /// <inheritdoc />
    string[] CommunicationMethods { get; }

}
