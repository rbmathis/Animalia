namespace AnimalKingdom.root.Metazoa.Chordata.Amphibia.Anura.Pelobatidae;

/// <summary>
/// Interface defining characteristics of Pelobatidae (family).
/// </summary>
public interface IPelobatidae
{
    /// <inheritdoc />
    string FamilyName { get; }

    /// <inheritdoc />
    string[] FamilyCharacteristics { get; }

    /// <inheritdoc />
    bool HasNonRetractableClaws { get; }

    /// <inheritdoc />
    bool IsDigitigrade { get; }

    /// <inheritdoc />
    int FrontToeCount { get; }

    /// <inheritdoc />
    int HindToeCount { get; }

    /// <inheritdoc />
    string[] CommunicationMethods { get; }

}
