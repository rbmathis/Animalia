namespace AnimalKingdom.root.Metazoa.Chordata.Aves.Charadriiformes.Burhinidae;

/// <summary>
/// Interface defining characteristics of Burhinidae (family).
/// </summary>
public interface IBurhinidae
{
    /// <inheritdoc />
    string FamilyName { get; }

    /// <inheritdoc />
    string[] FamilyCharacteristics { get; }

    /// <inheritdoc />
    bool HasNonRetractableClaws { get; }

    /// <inheritdoc />
    bool IsDigitigrade { get; }

    /// <inheritdoc />
    int FrontToeCount { get; }

    /// <inheritdoc />
    int HindToeCount { get; }

    /// <inheritdoc />
    string[] CommunicationMethods { get; }

}
