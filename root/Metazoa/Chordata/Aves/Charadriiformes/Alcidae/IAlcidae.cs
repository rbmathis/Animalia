namespace AnimalKingdom.root.Metazoa.Chordata.Aves.Charadriiformes.Alcidae;

/// <summary>
/// Interface defining characteristics of Alcidae (family).
/// </summary>
public interface IAlcidae
{
    /// <inheritdoc />
    string FamilyName { get; }

    /// <inheritdoc />
    string[] FamilyCharacteristics { get; }

    /// <inheritdoc />
    bool HasNonRetractableClaws { get; }

    /// <inheritdoc />
    bool IsDigitigrade { get; }

    /// <inheritdoc />
    int FrontToeCount { get; }

    /// <inheritdoc />
    int HindToeCount { get; }

    /// <inheritdoc />
    string[] CommunicationMethods { get; }

}
