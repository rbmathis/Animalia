namespace AnimalKingdom.root.Metazoa.Chordata.Aves.Piciformes.Indicatoridae;

/// <summary>
/// Interface defining characteristics of Indicatoridae (family).
/// </summary>
public interface IIndicatoridae
{
    /// <inheritdoc />
    string FamilyName { get; }

    /// <inheritdoc />
    string[] FamilyCharacteristics { get; }

    /// <inheritdoc />
    bool HasNonRetractableClaws { get; }

    /// <inheritdoc />
    bool IsDigitigrade { get; }

    /// <inheritdoc />
    int FrontToeCount { get; }

    /// <inheritdoc />
    int HindToeCount { get; }

    /// <inheritdoc />
    string[] CommunicationMethods { get; }

}
