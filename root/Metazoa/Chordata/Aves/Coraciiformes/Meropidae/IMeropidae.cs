namespace AnimalKingdom.root.Metazoa.Chordata.Aves.Coraciiformes.Meropidae;

/// <summary>
/// Interface defining characteristics of Meropidae (family).
/// </summary>
public interface IMeropidae
{
    /// <inheritdoc />
    string FamilyName { get; }

    /// <inheritdoc />
    string[] FamilyCharacteristics { get; }

    /// <inheritdoc />
    bool HasNonRetractableClaws { get; }

    /// <inheritdoc />
    bool IsDigitigrade { get; }

    /// <inheritdoc />
    int FrontToeCount { get; }

    /// <inheritdoc />
    int HindToeCount { get; }

    /// <inheritdoc />
    string[] CommunicationMethods { get; }

}
