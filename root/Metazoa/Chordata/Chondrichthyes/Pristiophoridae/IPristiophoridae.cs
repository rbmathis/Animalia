namespace AnimalKingdom.root.Metazoa.Chordata.Chondrichthyes.Pristiophoridae;

/// <summary>
/// Interface defining characteristics of Pristiophoridae (family).
/// </summary>
public interface IPristiophoridae
{
    /// <inheritdoc />
    string FamilyName { get; }

    /// <inheritdoc />
    string[] FamilyCharacteristics { get; }

    /// <inheritdoc />
    bool HasNonRetractableClaws { get; }

    /// <inheritdoc />
    bool IsDigitigrade { get; }

    /// <inheritdoc />
    int FrontToeCount { get; }

    /// <inheritdoc />
    int HindToeCount { get; }

    /// <inheritdoc />
    string[] CommunicationMethods { get; }

}
